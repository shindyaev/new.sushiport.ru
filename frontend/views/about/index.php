<script type="text/javascript">
	$(document).ready(function() {
		$(".about-gallery-up-arrow").click(function() {
			var top = parseInt($(this).parent().find(".about-gallery-slider-item-list-ul").css("top"));

			if (top < 0) {
				top += 140;
				$(this).parent().find(".about-gallery-slider-item-list-ul").animate({top: top});
			}
		})
		
		$(".about-gallery-down-arrow").click(function() {
			var top = parseInt($(this).parent().find(".about-gallery-slider-item-list-ul").css("top"));
			var count = $(this).parent().find(".about-gallery-slider-item-list-ul li").length;

			if (top > -(count - 3)*140) {
				top -= 140;
				$(this).parent().find(".about-gallery-slider-item-list-ul").animate({top: top});
			}
		})

		$(".about-gallery-slider-item-list-ul li a").click(function() {
			var src = $(this).attr("href");
			$(this).parent().parent().parent().parent().parent().parent().find(".about-gallery-main-img-cont img").attr('src', src);

			return false;
		})
	})
</script>
